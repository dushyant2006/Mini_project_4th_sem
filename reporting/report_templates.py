TEMPLATES = {
    "standard":  get_standard_template,
    "executive": get_executive_template,
    "technical": get_technical_template,
}


def generate_report(rca_report: Dict,
                    template: str = "standard") -> str:
    """
    Generates an incident report using the specified template.

    Args:
        rca_report: Structured RCA report dict
        template:   'standard' | 'executive' | 'technical'

    Returns:
        Formatted report string
    """
    generator = TEMPLATES.get(template, get_standard_template)
    return generator(rca_report)
